
//
// # Generic graph
//

var iper = require('iper');

var IperGraph = iper.IperGraph;

// create an empty graph
var graph1 = new IperGraph();

// we can add nodes and edges later, by default we have an hypergraph with no restriction
var id1 = graph1.createNode('foo');
var id2 = graph1.createNode('bar');
var id3 = graph1.createNode('quz');

// create some 2-edges
graph1.createEdge([id1, id2]);
graph1.createEdge([id2, id3]);
graph1.createEdge([id3, id1]);

// we have three nodes so we can create also an hyperedge
graph1.createEdge([id1, id2, id3]);

